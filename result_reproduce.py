import numpy as np

# Create 1D array
arr = np.arange(5, 10)
print('1D Array:', arr)

# Reshape to 1x5 matrix
matrix = arr.reshape(1, 5)
print('1x5 Matrix:\n', matrix)

# Mean, Median, Std
print('Mean:', np.mean(arr))
print('Median:', np.median(arr))
print('Standard Deviation:', np.std(arr))

# Element-wise operations
arr2 = np.arange(21, 26) # Changed arr2 to have 5 elements
print('Addition:', arr + arr2)
print('Multiplication:', arr * arr2)

"""# **Matplotlib**"""

import matplotlib.pyplot as plt

# Line Plot
x = list(range(11, 21))
y = [i**2 for i in x]
plt.plot(x, y)
plt.title('Numbers vs Squares')
plt.xlabel('Numbers')
plt.ylabel('Squares')
plt.show()

# Bar Chart
students = ['Ali', 'Babar', 'Ahmed', 'Zain', 'Mohid']
marks = [85, 90, 78, 95, 88]
plt.bar(students, marks)
plt.title('Student Marks')
plt.xlabel('Students')
plt.ylabel('Marks')
plt.show()

# Pie Chart
categories = ['Category A', 'Category B', 'Category C']
values = [30, 50, 20]
plt.pie(values, labels=categories, autopct='%1.1f%%')
plt.title('Category Distribution')
plt.show()